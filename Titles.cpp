void War() {
	cout << "\t\t\t WAR CARD GAME" << "\n";
	cout << "\n";
	cout << "    The objective of the game is to win all of the cards." << "\n";
	cout << "The deck is divided evenly among the players, giving each a down stack." << "\n";
	cout << "In unison, each player reveals the top card of their deck—this is a 'battle'—and the player" << "\n";
	cout << "with the higher card takes both of the cards played and moves them to their stack.Aces are high," << "\n";
	cout << "and suits are ignored.If the two cards played are of equal value, then there is a 'war'." << "\n";
	cout << "Both players place the next three cards face down and then another card face - up." << "\n";
	cout << "The owner of the higher face - up card wins the war and adds all the cards on the table to the bottom of their deck." << "\n";
	cout << "If the face - up cards are again equal then the battle repeats with another set of face - down / up cards." << "\n";
	cout << "This repeats until one player's face-up card is higher than their opponent's." << "\n";
	cout << "A six beats an ace" << "\n";
	cout << "\n";
}
